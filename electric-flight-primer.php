<td class="XSP_CENTER_PANEL" rowspan="2"><table class="XSP_CENTER_PANEL" cellpadding="0" cellspacing="0" border="0"><tr><td class="XSP_MAIN_PANEL"><p class="title"><span style="FONT-SIZE: 20pt"><font color="#0000ff"><strong>Electric Flight
Primer</strong></font></span></p>

<p><em>Frank Weaver, PDQ Flyers</em></p>

<p>This topic resulted from trying to help those who are new to the hobby of flying electric powered model
aircraft, as well as those who are involved and not seeing any success for their efforts. With all the new
technology and products available to us &ldquo;E&rdquo; modelers, such as brushless motors and lipoly batteries, it has become
much easier in the last few years, but you still have to be willing to learn how to apply the principals and the
math to make it all work. I&rsquo;m not about to explain it all here as that would take up all my hobby time. Besides,
part of the fun is learning new things. So, here are a few guidelines as to where to start learning what you need
to know.<br>
<br>
To achieve success with electric power, there are three ways to proceed………<br>
<br>
&nbsp; (1) Buy something off the shelf that the manufacture has tested and follow the instructions to the
letter.<br>
&nbsp; (2) Copy what others have done if they have a model that has the performance you are looking for.<br>
&nbsp; (3) Learn everything about this form of power that&rsquo;s available and how to apply it, then &lsquo;roll your
own&rsquo;.<br>
<br>
Number one does not quarantee success, as there are too many variables that the modeler cannot duplicate exactly
and the performance expected usually is overstated in the manufacturer&rsquo;s advertisements. This is not applicable if
you followed rule number three.<br>
<br>
Number two does offer success, but only if you copy exactly to the smallest detail, what someone else has done.
But, this method will only benefit you if you apply number three and ask questions to find out why this modeler
used the items that he did and what part they play in making the system work.<br>
<br>
Ah………..I think we&rsquo;re going somewhere here, yup, # 3, preferred by 10 out of 10 successful &ldquo;E&rdquo; pilots the world
over.<br>
<br>
Number three requires a desire to learn and a willingness to put in the effort. Once you have figured it out and
apply your new found knowledge, and see the results in a practical application, the fun and satisfaction level will
make you want to feed those grey cells with even more info on electrons.<br>
There&rsquo;s lots of avenues out there for information: model magazines, the internet - <a href=
"http://www.canadianelectricflight.com/">Canadian Electric Flight</a>&nbsp;and <a href=
"http://www.ezonemag.com/">e-zone magazine</a>, and asking questions of experienced &ldquo;E&rdquo; modelers.<br>
The information printed in magazines or manufacturers&rsquo; web sites can be considered accurate, but be careful of what
some &lsquo;so called experts&rsquo; post in discussion forums on the net. I have read misinformation on the net that has not
been corrected for weeks.<br>
Also, don&rsquo;t assume that salesman on the other end of the phone at the mail-order hobby shop is steering you in the
right direction either. He is a salesman, and is out of stock on the motor you really need, so guess
what…………????<br>
So, be prepared to spend many hours reading, pondering, thinking, calculating and eventually it will come to you.
Just like that light bulb, all of a sudden it will come on and you will be able to walk the walk and talk the
talk.<br>
Asking an experienced modeler at the local flying field has the benefit of seeing your answers demonstrated. You
know, a picture is worth……………….<br>
<br>
The very first item any electric modeler requires and is considered a definite MUST HAVE, is an Astro Whatmeter or
equivalent device, which is required to see the math in action. (amps X volts = watts )<br>
It insures you are that you have achieved the required power needed and are not about to damage any of the
components of your drive system, the motor, the speed control or the batteries.<br>
IF YOU ARE USING LIPO BATTERIES, YOU SHOULD READ EVERYTHING YOU CAN FIND ON THEM, GET TO KNOW ALL ABOUT WHAT CELLS
YOU HAVE, WHAT THEY CAN/CAN&rsquo;T DO FOR YOUR SYSTEM AND HOW TO CARE FOR THEM.<br>
While we&rsquo;re on the subject, make sure you check the individual cell voltages on a regular basis. Why? Well, look it
up on the net, it&rsquo;s very important that you know what to do if they are not balanced and the consequences.<br>
<br>
In your quest for knowledge, keep in mind that, with electric, the calculations begin with the prop, as it&rsquo;s the
thrust that flys the model, not the watts or even the RPM. OK, OK, yes, we do want to know the horsepower/watts it
will put out, and the RPM may be interesting, but they are only guide lines, and only &lsquo;real world testing&rsquo; by
flying the model will tell if you have it right. So here&rsquo;s a list of what to look for……….<br>
<br>
How big a prop do I use, and how fast do I want to spin it: do I want speed or thrust?<br>
<br>
What kind of power will my motor deliver to turn that prop, and how many amps will it handle?<br>
<br>
What KV ( RPM per volt) is my motor, and how does that relate to the prop, gearing and batteries?<br>
<br>
If using a gear box, what gear ratio, and how does this affect the amps?<br>
<br>
How many amps will my batteries deliver?<br>
<br>
Since volts tell the motor how fast to turn, how many volts do I need?<br>
<br>
Am I using the proper electronic speed control, and is it programmed properly?<br>
<br>
Will my plugs and wires handle the amps?<br>
<br>
There&rsquo;s loads more, but this is a start…………<br>
<br>
Now that you have figured all that out, don&rsquo;t stop now, because there&rsquo;s more, lots more. Now we get into making
your power system fly your model and perform the way you expect. The next important factor is the weight of the
model and its wing loading. Never, never, NEVER add weight to an electric model to balance it: move something, even
if it requires a rebuild of the nose of the plane. To add weight is to make everything you have learned to this
point null and void. Anyways, you will probably get struck by lightning at the flying field by the &ldquo;E&rdquo; gods if they
catch you with a chunk of lead stuck on your firewall. All that time reading will be wasted, and your widow will
sell off your modeling junk for peanuts to get even when she finds out what you really paid for it.<br>
<br>
Now, check out Mo Alam&rsquo;s article on electric flight at <a href=
"http://www.pdqflyers.com/e_flight/eflight_planner.php">Electric Flight - My Key Learnings</a><br>
<br>
Keep in mind these two rules of thumb, and it&rsquo;s the thrust to weight ratio that&rsquo;s the most important consideration
for a great flying electric model. Also, be prepared to be the proud owner of a very large collection of
propellers.<br>
<br>
(1) If you are using round cells, to save an ounce is equivalent to adding one cell to your battery pack in
performance.<br>
<br>
(2) Watts per pound……<br>
<br>
&nbsp; 50 watts per pound = trainer<br>
<br>
&nbsp; 75watts per pound = sport aerobatics<br>
<br>
&nbsp; 100 watts per pound = 3D<br>
<br>
The only place that &lsquo;success&rsquo; comes before &lsquo;work&rsquo;, is in the dictionary, so plug away and your efforts will be
rewarded with a great flying model airplane powered by electrons.<br>
<br>
Personally, I have read hundreds of kit, motor, ESC, gear box and battery reviews. Not that I ever intended to buy
all these products, but I was interested in how they worked and why. I have read thousands of posts on the
internet, spending many hours at the computer into the wee hours. Yes, it took time, but time well spent, and has
made the hobby even more enjoyable now that I actually know what I&rsquo;m doing, and I&rsquo;m having the most fun ever in
thirty five years of RC modeling. I can even wind my own motors and get it right the first time and, just think,
not long ago I thought a watt was an adjective. So there is hope; if I can do it, so can you.</p><br>

<h4 class="bold center">Fly SMALL, have a BALL…….FrankE</h4>
