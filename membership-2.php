<h4 wrap="">Renewing a PDQFlyers membership</h4>

<p wrap="">Renewing members&nbsp;must update their database entry by going to the <a href=
"member-data-update.php">Membership Data Update</a> page and&nbsp;correcting any contact information that is out
of&nbsp;date. Current contact information is important in case of an accident and is required by the owners of our
field, the Department of National Defense.</p>

<p wrap="">After confirming your details you&nbsp;can apply for membership and arrange payment just as&nbsp;is
described above for&nbsp;new members. Once payment&nbsp;and current MAAC membership&nbsp;have been confirmed a new
membership card will be issued.</p>

<h2 align="left"><span style="FONT-SIZE: 12pt"><span style="FONT-SIZE: 12pt">The Importance of our
Rules</span></span></h2>

<p align="left"><span style="FONT-SIZE: 12pt"><span style="FONT-SIZE: 12pt">Some people&nbsp;feel that rules are
merely guidelines, to be rigorously followed only by the anal and not by free-thinking people like
themselves.&nbsp; If you are one of these, DON'T join our Club.</span></span></p>

<p align="left"><span style="FONT-SIZE: 12pt"><span style="FONT-SIZE: 12pt">We have gone to some trouble to ensure
that our Rules are reasonable and ALL have one of three objects in mind:</span></span></p>

<ul>
    <li>
        <div align="left">
            Safety
        </div>
    </li>

    <li style="LIST-STYLE-TYPE: none">
        <ul>
            <li>
                <div align="left">
                    Most of our aircraft use powerful engines which spin an object with sharp edges at high speeds
                    that can inflict serious damage to the human body if an accident occurs. We have to be very
                    careful to make sure that this doesn't happen.
                </div>
            </li>
        </ul>
    </li>

    <li>
        <div align="left">
            Continued access to our Flying Field
        </div>
    </li>

    <li style="LIST-STYLE-TYPE: none">
        <ul>
            <li>
                <div align="left">
                    Our agreement with the DND gives us access to an enviable location and is subject to the
                    Military's good will. It is extremely important that we abide by their rules and maintain their
                    property in a way that they find acceptable.
                </div>
            </li>
        </ul>
    </li>

    <li>
        <div align="left">
            Neighbour Relations
        </div>
    </li>

    <li style="LIST-STYLE-TYPE: none">
        <ul>
            <li>
                <div align="left">
                    The major cause of the loss of a flying facility is neighbour complaints, usually about noise.
                    Although our nearest neighbours are not overly close, it is still important that we don't
                    disturb them with our activites.
                </div>
