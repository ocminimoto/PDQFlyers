<h4>General</h4>

<p align="left">PDQ Flyers is a <a href="http://www.maac.ca/en/"
   target="_new">MAAC</a> (Model Aeronautics Association of Canada) affiliated club which means that members must
   also maintain membership of MAAC. Members must follow both the <a href=
   "http://www.maac.ca/en/docs/2015%20MAAC%20RC%20Scale%20Rules%20-%20Final.pdf"
   target="_new">MAAC rules</a>&nbsp;and&nbsp;<a href="pdq-rules.php">PDQ Flyers&nbsp;rules</a>. Being part of MAAC
   provides us with insurance that protects the club and its members, provides us a representation at local,
   national and international government levels, and links us with other model flying clubs around the country.</p>

<h4 align="left"><span style="FONT-SIZE: 12pt">Membership Types</span></h4>

<ul>
    <li>
        <div wrap="">
            <span style="FONT-SIZE: 12pt">Full Member (adult): the basic membership for those&nbsp;who wish to fly
            with us</span>&nbsp;
        </div>
    </li>

    <li>
        <div wrap="">
            Full Member (junior): persons under 18 years old on January 1st who wish to fly with us
        </div>
    </li>

    <li>
        <div wrap="">
            Social Member: any person that wants to take part in club activities but does not fly
        </div>
    </li>
</ul>

<h4 align="left"><span style="FONT-SIZE: 12pt">Membership Fees</span></h4>

<ul>
    <li>
        <div wrap="">
            <span>Annual Full Adult membership: $100 per year</span>&nbsp;
        </div>
    </li>

    <li>
        <div wrap="">
            New Full Adult Member initiation fee: $50 one time
        </div>
    </li>

    <li>
        <div wrap="">
            Annual Full Junior membership:&nbsp;free
        </div>
    </li>

    <li>
        <div wrap="">
            Annual Social membership: $30 per year
        </div>
    </li>
</ul>

<p wrap=""><span>If a field key is requested, there is a $5 field key deposit.</span></p>

<p wrap=""><span><span>All memberships run for a calendar year and the fee&nbsp;for continuing membership comes
due&nbsp;at the beginning of the year. To encourage prompt payment any continuing full adult member will
receive&nbsp;a rebate of $25 if their fee is paid before February 1st.</span></span></p>

<p wrap="">First year Full Adult membership fee is prorated for when in the year you join. Adjusted fees are as
follows:</p>

<ul>
    <li>
        <div wrap="">
            join for the full year, $75 membership plus $50 initiation
        </div>
    </li>

    <li>
        <div wrap="">
            join after July 1st, $50 membership plus $50 initiation
        </div>
    </li>

    <li>
        <div wrap="">
            join after September 1st, $25 membership plus $50 initiation
        </div>
    </li>
</ul>

