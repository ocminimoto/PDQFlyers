<td class="XSP_CENTER_PANEL" rowspan="2"><table class="XSP_CENTER_PANEL" cellpadding="0" cellspacing="0" border="0"><tr><td class="XSP_MAIN_PANEL"><p><span style="FONT-SIZE: 20pt"><strong><font color="#0000ff">Secutity and Privacy</font></strong></span></p>

<h4>Personal Data&nbsp;Availabilty&nbsp;</h4>

<p>The PDQ Flyers Club maintains an online data base containing Member's contact information and preferences, plus
photographs as individuals or in groups.&nbsp; We do not have access to (and&nbsp;therefore do not maintain)
financial information&nbsp;entered during fees payment.</p>

<p>If you are&nbsp;sensitive&nbsp;about any of this information being on the internet, then this next
section&nbsp;may be of interest to you.</p>

<h4>Web Search Engines</h4>

<p>These are known as "web crawlers", "spiders", "robots", and various other names, but all of them come in one
of&nbsp;two flavours: those that play by the rules and those that don't.&nbsp; The latter are usually interested in
either financial information or email addresses for SPAM targets.</p>

<p>All of these searches&nbsp;read and parse&nbsp;individual web pages (which are available to anyone)&nbsp;looking
for whatever information&nbsp;they're interested in collecting.&nbsp; Most web pages&nbsp;are readable by a
computer easily as they are&nbsp;in the same text form that you see on your browser screen.</p>

<p>A factoid for you: there are approximately 100 million websites on the internet and Google monitors 50 billion
pages, reading them within days of their changing.</p>

<p>On entry to a site, all honest web searchers check&nbsp;for a file called "robot.txt",&nbsp;which tells them if
there are areas on the site where you do not want them to look.</p>

<p>To foil access from search engines&nbsp;who do not follow the&nbsp;rules, with a little extra effort, we can
keep data on the server and only make it available to the&nbsp;browser when it wants to show it on the
screen.&nbsp; This is called "server side" as opposed to "client side" data.&nbsp; Since this doesn't show up on
the web page read by the search engine, the information is kept private.&nbsp;</p>

<h4>PDQ Flyers</h4>

<p>On our site, we give&nbsp;search engines&nbsp;free access to our main site, but our Member's Section is off
limits.&nbsp; If you do a Google search of yourself, you may find your name popping up as the member of the Noise
Committee (for example), but your Member Page with your name and particulars will not.</p>

<p>In addition, all personal contact information (both on the main site and the Member's Section) is "server
side".&nbsp; It cannot be seen by web searchers.</p>

<p>Our data base is only available through passwords and is as secure as the security mechanisms of HostPapa can
make it.&nbsp;</p>

<h4>Privacy</h4>

<p>For those that are still concerned about the public&nbsp;availabilty of their personal&nbsp;information or
photos, we have provided Privacy Preferences on the Data Update form which allows you to indicate that you do want
the following to appear in the Member's Section:</p>

<ul>
    <li>address - if this is checked, your address will be blank</li>

    <li>phone - if this is checked, your&nbsp;phone numbers&nbsp;will be blank</li>

    <li>name - if this is checked, there will be no Member Page created for you</li>

    <li>photo - if this is checked, your headshot&nbsp;will not be included in the Member's Gallery</li>
</ul>

<p>I hope that this explanation is sufficient to alleviate any concerns and that you do not check these Privacy
boxes.&nbsp; Fellow members use this infomation to contact you (or in the case of photos, to recognize you on the
field!) and appreciate its availabilty.</p>

<p>Please contact <a style="TEXT-DECORATION: underline; COLOR:"
   href="mailto://webmaster@pdqflyers.com">the webmaster</a> if this explanation raises questions and you would
   like to discuss it.</p>

