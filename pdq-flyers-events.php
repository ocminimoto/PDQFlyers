<h5 style="MARGIN: 0cm 0cm 0pt"
    class="MsoNormal"><span style="FONT-SIZE: 12pt">Notes:</span></h5>

<h5 style="MARGIN: 0cm 0cm 0pt"
    class="MsoNormal">&nbsp;</h5>

<p style="MARGIN: 0cm 0cm 0pt"
   class="MsoNormal">&nbsp;<span style="FONT-SIZE: 12pt"><strong><em><a name="springfunfly"></a>Spring Fun
   Fly:</em></strong></span></p>

<p style="MARGIN: 0cm 0cm 0pt"
   class="MsoNormal"><span style="FONT-SIZE: 12pt">Hot dogs, pop and chips will be provided for free,
   and&nbsp;there may&nbsp;be enough money left over for a few modest prizes. Competition will be low key with
   about five simple events such as- the most loops in 1 min, spot landings ect.&nbsp; Flying should start around
   10:30 so please be on time to start.&nbsp; Current M.A.A.C. is mandatory. The field will be available
   again&nbsp;for open flying as soon as the Fun Fly is finished, around mid afternoon.</span></p>

<p style="MARGIN: 0cm 0cm 0pt"
   class="MsoNormal">&nbsp;</p>

<p style="MARGIN: 0cm 0cm 0pt"
   class="MsoNormal"><span style="FONT-SIZE: 12pt"><strong><em>FLoat Fly:</em></strong></span></p>

<p style="MARGIN: 0cm 0cm 0pt"
   class="MsoNormal"><span style="FONT-SIZE: 12pt">The PDQ&nbsp;club held weekend&nbsp;Float Fly events at Nanaimo
   Lake for several years, where we invited flyers from other clubs to attend. &nbsp;However, the lake is usually
   very windy and the rent was high.&nbsp; At the last event in 2010 we only had 5 flyers attend and&nbsp;they
   could only fly for about a half day, so...&nbsp; last year we established an agreement with the owner of a site
   on Quennel Lake in Cedar, which allows us to fly every Saturday from 10am to 4pm.&nbsp;&nbsp;Since the site is
   very limited in size, with only room for about 6 or 7 cars and&nbsp;has no facilities, we cannot use it as an
   event site.</span></p>

