<td class="XSP_CENTER_PANEL" rowspan="2"><table class="XSP_CENTER_PANEL" cellpadding="0" cellspacing="0" border="0"><tr><td class="XSP_MAIN_PANEL"><h2>Field Rules</h2>

<h4>Nanoose Field Lease</h4>

<p>PDQ Flyers is fortunate to be allowed access to our main flying field from the DND at a very modest rate. The
field covers approximately 50 of 220 DND acres and our lease is for shared usage with (occasional) military
activities taking precedence. The lease includes a liability waiver so our MAAC insurance coverage is
essential.</p>

<p>Since the DND are motivated to provide this to us as a community service, it is extremely important that our
members ensure that the agreed procedures and expectations be met or exceeded. The lease has a 5 year term but it
may be cancelled at DND pleasure.</p>

<p>Rules of use:&nbsp;</p>

<ul>
    <li>
        <div>
            <span><span>Everyone&nbsp;is required to sign the DND waiver form (one time only).</span></span>&nbsp;
        </div>
    </li>

    <li>
        <div>
            <span>The entrance gate must be closed and locked if none of our members&nbsp;are on the
            field.</span>&nbsp;
        </div>
    </li>

    <li>
        <div>
            The DND gatehouse needs to know if we are using the field so they must be notified on gate opening and
            closing by calling them at 250-468-5060. (There is a card on the Frequency Board that should be turned
            over to let everyone know that someone has called in/out).&nbsp;
        </div>
    </li>

    <li>
        <div>
            <span>The first to arrive must unlock and open the gate using the PDQ lock (the mechanism is a dual
            lock with DND using the other lock).</span>&nbsp;
        </div>
    </li>

    <li>
        <div>
            <span><span>The entrance gate must be left partially closed to ensure that the private property sign is
            in clear evidence.</span></span>&nbsp;
        </div>
    </li>

    <li>
        <div>
            <span>The last person out must re-lock the gate.</span>&nbsp;
        </div>
    </li>

    <li>
        <div>
            <span><span>Environmental care must be taken (ie; no tree chopping, no garbage left,
            etc).</span></span>&nbsp;
        </div>
    </li>
</ul>

<h4>MAAC</h4>

<p>MAAC has gone to a great deal of trouble to define best practices in our hobby and have documented
rules,&nbsp;guidelines, and R/C Field Etiquette to ensure that flyers and spectators are safe,&nbsp;that problems
are minimized, and that we can enjoy our hobby. In addition, our insurance coverage is through MAAC and
therefore&nbsp;we need to know and must abide by their rules.</p>

<p>A copy of the MAAC&nbsp;rules and guidelines can be found <a href=
"http://www.maac.ca/docs/maac_safety_code_e.pdf"
   target="_new">here</a>.</p>

<p>All Pilots must have a current MAAC membership and a membership card is required to fly.</p>

<h4>Field Use</h4>

<ul>
    <li>Guests may visit the field a maximum of three times per year, not counting events. lf they visit more
    often, they are expected to join the club. Current A.M.A. members are welcome.</li>

    <li>Parking in the parking lot only, behind the marked area which is 100' from flight line.</li>

    <li>No Vehicles in the pit area at any time, other than work crews.</li>

    <li>All children and pets must be kept under control at all times. Pets on a leash.</li>

    <li>There is minimal garbage disposal - if you brought it take it home.</li>

    <li>Absolutely no alcohol permitted on the premises.</li>
</ul>

<h4>Transmitter Impound and Frequency Board</h4>

<ul>
    <li><span>All non-Spread Spectrum (ie, not 2.4GHz)&nbsp;transmitters must be placed in the impound area upon
    arrival at the field and&nbsp;a</span> Frequency Pin displaying your name and
    transmitter&nbsp;channel&nbsp;number clipped to the frequency board.&nbsp; The <span>Frequency Pin must be
    moved to</span>&nbsp;its correct frequency location in order to remove the transmitter for any reason other
    than to leave the field (only the pilot may move the pin).&nbsp;</li>

    <li>All flyers must clip their current MAAC and club cards to the frequency board while flying.&nbsp;</li>

    <li>Please limit non-2.4 frequency use to 15 minutes.&nbsp;</li>

    <li>Remove the Frequency Pin and membership cards from the board when no longer flying.&nbsp;</li>
</ul>

<h4>Flight Procedures</h4>

<ul>
    <li>Flight tables are on a first come basis&nbsp;(make room if necessary).</li>

    <li>Concrete pads behind a protective screen&nbsp;indicate where pilots are to stand when flying their
    models.&nbsp;</li>

    <li>Engines are not to be started&nbsp;if the plane is&nbsp;facing the pilot pads.&nbsp;&nbsp;</li>

    <li>Models are not to be&nbsp;taxied in the pit area and are to be carried to and from the pilot
    stations.&nbsp;</li>

    <li>Do not fly&nbsp;on the spectator side of&nbsp;the flight line! (the flight line is 20 feet in front of
    pilot stations and&nbsp;extends the full length of the mowed field).&nbsp;</li>

    <li><span>If other aircraft are in the air, fly in the same circuit direction.</span>&nbsp;</li>

    <li>Communicate your intentions "<font color="#ff0000">loudly</font>" so others on the flight line can hear
    you:&nbsp;"<font color="#ff0000">On the field</font>", "<font color="#ff0000">Landing from left</font>",
    "<font color="#ff0000">Landing from right</font>", "<font color="#ff0000">Taking off from the left</font>",
    "<font color="#ff0000">Taking off from the right</font>". If you get into trouble "<font color="#ff0000">Shout
    it out</font>".</li>

    <li>Members who have not yet received their "Wings" must fly under the supervision of a club instructor and
    follow the club&nbsp;<span>training program (<a href="training-program.php">Wings
    Program</a>).</span>&nbsp;</li>
</ul>

<h4>Jet Turbine Aircraft&nbsp;&nbsp;</h4>

<p>RC jet turbine aircraft are permitted to&nbsp;be flown&nbsp;subject to the following rules:</p>

<ul>
    <li>Never fly a turbine jet aircraft when a fire ban &ldquo;high&rdquo;&nbsp;or &ldquo;extreme&rdquo; are active in the area;</li>

    <li>Follow the rules and guidelines provide in the BC Government Wildfire Management Branch website,
       <a href="http://bcwildfire.ca/"
       target="_new">located here</a>;</li>

    <li>Always fly with a spotter;</li>

    <li>Airplane can fly only after have been inspected by the safety committee of PDQ Flyers;</li>

    <li>Two people must be ready with 5lb CO fire extinguishers every time a turbine jet is flying;</li>

    <li>One working cell phone must be present and available;</li>

    <li>Only concealed flameless (compressor equipped) turbine aircraft allowed and only with horizontal jet
    blast;</li>

    <li>Only aircraft equipped with concealed Kevlar fuel tank allowed;</li>

    <li>Aircraft have to be equipped with a mechanical fuel shut off and electric fuel feed valve &ndash; computer
    managed;</li>

    <li>No starting propane bottle installed on the aircraft allowed; and</li>

    <li>Maximum 4 litres of&nbsp;fuel on board allowed</li>
</ul>

<h4>Also check out <a href="noise-regulations.php">Noise Regulations</a></h4>
