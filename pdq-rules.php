<td class="XSP_CENTER_PANEL" rowspan="2"><table class="XSP_CENTER_PANEL" cellpadding="0" cellspacing="0" border="0"><tr><td class="XSP_MAIN_PANEL"><p><strong><font color="#0000ff"><span style="FONT-SIZE: 20pt">PDQ Rules</span></font></strong></p>

<h4><span style="FONT-SIZE: 12pt">Nanoose Field Lease</span></h4>

<p>PDQ Flyers is fortunate to be allowed access to our main flying field from the DND at a very modest rate. The
field covers approximately 50 of 220 DND acres and our lease is for shared usage with (occasional) military
activities taking precedence. The lease includes a liability waiver so our MAAC insurance coverage is
essential.</p>

<p>Since the DND are motivated to provide this to us as a community service, it is extremely important that our
members ensure that the agreed procedures and expectations be met or exceeded. The lease has a 5 year term but it
may be cancelled at DND pleasure.</p>

<h4>MAAC</h4>

<p>MAAC has gone to a great deal of trouble to define best practices in our hobby and have documented rules,
guidelines, and R/C Field Etiquette to ensure that flyers and spectators are safe, that problems are minimized, and
that we can enjoy our hobby. In addition, our insurance coverage is through MAAC and therefore we need to know and
must abide by their rules.</p>

<p>A copy of the MAAC rules and guidelines can be found <a style="TEXT-DECORATION: underline; COLOR:"
   href="http://www.maac.ca/docs/maac_safety_code_e.pdf"
   target="_new">here</a>.</p>

<p>A copy of the PDQ Flyers rules can be found <a style="TEXT-DECORATION: underline; COLOR:"
   href="PDF-Files/PDQ-Rules.pdf"
   target="_new">here</a>.</p>

<h2>PDQ Flyers Rules</h2>

<ol>
    <li>
        <div align="left">
            <span style="FONT-SIZE: 12pt; LINE-HEIGHT: 115%"><span style="FONT-SIZE: 12pt">All pilots must have a
            current M.A.A.C. membership, copies of M.A.A.C. and P.D.Q. Flyers membership cards must be attached to
            your frequency pin.</span></span>&nbsp;
        </div>
    </li>

    <li>
        <div align="left">
            <span style="FONT-SIZE: 12pt">All M.A.A.C. and P.D.Q. Flyers Rules must be obeyed at all times, no
            exceptions.</span>&nbsp;
        </div>
    </li>

    <li>
        <div align="left">
            <span style="FONT-SIZE: 12pt"><strong>EVERYONE</strong></span>&nbsp;<span style="FONT-SIZE: 12pt">is
            required to sign the DND waiver form (one time only).</span>&nbsp;
        </div>
    </li>

    <li>
        <div align="left">
            <span style="FONT-SIZE: 12pt">M.A.A.C. Frequency Board to be used at all times.</span><span style=
            "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">Transmitter and Frequency Pins for
            72 MHZ radios must be kept in the impound area when not flying.</span><span style=
            "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">2.4 GHZ radios exempt from impound
            policy, but pins must be attached to Frequency Board.</span><span style=
            "FONT-SIZE: 12pt">&nbsp;&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">Be respectful to others, limit
            flights to 15 minutes if sharing a frequency.</span>&nbsp;
        </div>
    </li>

    <li>
        <div align="left">
            <span style="FONT-SIZE: 12pt">Absolutely no flying behind the flight line, which is end to end of the
            cut grass field.</span><span style="FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style=
            "FONT-SIZE: 12pt">Aircraft flying high alpha maneuvers commonly know as 3D can come
            no</span><span style="FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">closer to the
            pits than the centre line of the runway (approximately 30' in front of the flight line).</span>&nbsp;
        </div>
    </li>

    <li><span style="FONT-SIZE: 12pt">Communicate your intentions</span>&nbsp;<span style=
    "FONT-SIZE: 12pt">&ldquo;LOUDLY&rdquo;</span><span style="FONT-SIZE: 12pt">&nbsp;so others on the flight line can hear
    you</span><span style="FONT-SIZE: 12pt">!</span><span style=
          "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">&ldquo;ON THE FIELD&rdquo;, &rdquo;LANDING FROM THE
          LEFT", &ldquo;LANDING FROM THE RIGHT&rdquo; , &ldquo;TAKE OFF FROM THE LEFT&rdquo;, &ldquo;TAKING OFF FROM THE
          RIGHT.</span>&nbsp;<span style="FONT-SIZE: 12pt">If you get in trouble:</span>&nbsp;<span style=
          "FONT-SIZE: 12pt">&ldquo;SHOUT IT OUT.&rdquo;</span></li>

    <li><span style="FONT-SIZE: 12pt">Noise Rules: (Electric Exempt)</span><span style=
    "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;</li>

    <li style="LIST-STYLE-TYPE: none">
        <ul>
            <li><span style="FONT-SIZE: 12pt">No starting of engines before</span> <span style=
            "FONT-SIZE: 12pt">10AM</span><span style="FONT-SIZE: 12pt">&nbsp;or after sunset.</span>&nbsp;</li>

            <li><span style="FONT-SIZE: 12pt"><span style="FONT-SIZE: 12pt">Maximum 5 aircraft at one time
            before</span> <span style="FONT-SIZE: 12pt">5 PM</span><span style="FONT-SIZE: 12pt">&nbsp;and 3
            after</span> <span style="FONT-SIZE: 12pt">5PM</span><span style="FONT-SIZE: 12pt">.</span></span></li>

            <li><span style="FONT-SIZE: 12pt">Aircraft must not exceed 92 db @ 6 meters, m</span><span style=
            "FONT-SIZE: 12pt">easured on the &ldquo;A&rdquo; weighting scale at the loudest orientation</span>&nbsp;</li>

            <li><span style="FONT-SIZE: 12pt">RULE EXEMPTION :&nbsp; 95db @ 6 meters with the following 3
            conditions:</span>&nbsp;</li>

            <li style="LIST-STYLE-TYPE: none">
                <ul>
                    <li><span style="FONT-SIZE: 12pt">Only one aircraft in the air,</span></li>

                    <li><span style="FONT-SIZE: 12pt">Pilots agree to stand down, and</span></li>

                    <li><span style="FONT-SIZE: 12pt">Never after</span><span style="FONT-SIZE: 12pt">5
                    PM</span><span style="FONT-SIZE: 12pt">.</span>&nbsp;</li>
                </ul>
            </li>

            <li><span style="FONT-SIZE: 12pt">All</span>&nbsp;<span style="FONT-SIZE: 12pt">Gas/Glow engines must
            be equipped with a proper muffler and current DB&nbsp; sticker attached in plain view, no exemptions
            for club members</span><span style="FONT-SIZE: 12pt">.</span></li>
        </ul>
    </li>

    <li><span style="FONT-SIZE: 12pt">Fail Safe must be configured for either throttle off or low throttle position
    if radio is capable.</span></li>

    <li><span style="FONT-SIZE: 12pt">All garbage must be taken away by whoever brought it.</span></li>

    <li><span style="FONT-SIZE: 12pt">Guest flyers may visit our field 3 times per year (excluding
    events).</span><span style="FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">If you visit more
    often you are expected to join the club.</span><span style=
          "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">Current</span><span style=
          "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">A.M.A.</span><span style=
          "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">members</span><span style=
          "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">are welcome.</span></li>

    <li><span style="FONT-SIZE: 12pt">Until Pilots have been trained and certified,</span><span style=
    "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">all flying will be under Certified Club
    Instructors using the</span><span style="FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style=
    "FONT-SIZE: 12pt">"Buddy Box Training System."</span><span style=
    "FONT-SIZE: 12pt">&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">Instructors are not to be held responsible
    for damage to a student&rsquo;s equipment, if there is a accident.</span></li>

    <li><span style="FONT-SIZE: 12pt">All Children and Pets must be kept under control at all times. Pets on a
    leash.&nbsp;</span>&nbsp;<span style="FONT-SIZE: 12pt">During any active flying, Pets are restricted from the
    pits and pilot shack areas.</span></li>

    <li><span style="FONT-SIZE: 12pt">Absolutely no alcohol permitted on premises.</span></li>

    <li><span style="FONT-SIZE: 12pt">Parking in parking area only.</span>&nbsp;<span style=
    "FONT-SIZE: 12pt">Respect handicap parking area.</span> <span style="FONT-SIZE: 12pt">As per M.A.A.C. rules no
    parking within 100 feet of the flight line.</span> However, loading and unloading aircraft and accessories is
    permitted (at your own risk)&nbsp;in a loading zone adjacent to the tables for a MAXIMUM duration of 5
    minutes.</li>

    <li><span style="FONT-SIZE: 12pt">First one at the field is responsible for ensuring that someone phones in to
    the gate house to advise them that the field is in use.&nbsp;</span> <span style="FONT-SIZE: 12pt">The last one
    to leave the field is responsible to make sure that the gate house has been advised that everyone has left the
    premises.</span></li>

