<h3>Membership Application and Membership Data Update</h3>

<p>The application process includes the data update process, with a little preamble and then the actual application
tacked on to the end.&nbsp;</p>

<p>Application&nbsp;is initiated from the page "Membership" by means of a link to "Member Data Update", both of
which are&nbsp; xSitePro pages listed&nbsp;on the menu.&nbsp; If the member just wants to update his data, he
starts at the latter.</p>

<p>"member-data-update" takes the requestor's MAAC number and last name and calls "data-updater", a stand-alone web
page.&nbsp; This program retrieves all of the persons data and displays it asking&nbsp;the&nbsp;requestor
to&nbsp;make&nbsp;any desired changes.&nbsp; It then calls "data-write" which makes the changes and then, if a
membership application is desired,&nbsp;asks for&nbsp;that information.&nbsp; It then passes control to
"app-submit", another hand-crafted web page, which constructs an email to the Treasurer advising of the
application.&nbsp; Then an online payment option is provided via a link to PayPal.</p>

<p>PayPal does its thing and transfers control to either "Payment Accepted" or "Payment Cancelled", both xSitePro
pages (that are not listed in the menu).</p>

<h3>Member Data Access for Executives</h3>

<p>The purpose of this function is to allow those few in the Club who need it, detailed information about our
membership.&nbsp; This includes their MAAC number and contact information.</p>

<p>The starting point is "Member Data Access", a published page in this xSitePro data base, but which is not
included in the menu.&nbsp; It takes some basic information and then calls "data-detail-exec", a stand-alone,
hand-crafted&nbsp;page which processes the request and produces a report.</p>

<p>The report is not provided unless the requestor's MAAC number is on the list and a password is required.&nbsp;
That password will be changed every January 1st.</p>

<h3>Group Broadcast eMails</h3>

<p>The xSitePro page "Group Broadcast" takes details of the message and who to send it to.&nbsp; You can request a
message be sent and to which interest group, a mailing list be created with or without comma separators, or all of
the above.&nbsp; It then calls "broadcast-action", a&nbsp;hand-crafted page&nbsp;to get this done.</p>

<h3>Webmaster management</h3>

<p>New members must ask the webmaster to add their MAAC number and last name to our member's&nbsp;database.&nbsp;
This is done through phpMyAdmin on the HostPapa cPanel.</p>

<p>Every January, the password for the Executive data access and the Club wide group broadcast password need to be
changed.</p>

<p>Data base backups need to be taken monthly and a restore process has to be in place.</p>
