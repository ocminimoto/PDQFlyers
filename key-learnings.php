<td class="XSP_CENTER_PANEL" rowspan="2"><table class="XSP_CENTER_PANEL" cellpadding="0" cellspacing="0" border="0"><tr><td class="XSP_MAIN_PANEL"><p class="title"><span style="FONT-SIZE: 20pt"><strong><font color="#0000ff">Key
Learnings</font></strong></span></p>

<p><em>Mo Alam, PDQ Flyers</em></p>

<p>The popularity and technology of electric radio controlled flight seems to be progressing at an electrifying
rate (pun intended!). It has taken Europe by storm, where it is now the predominant form of r/c flight. In
countries like Germany, Czech Republic and Italy, where glow flying fields are scarce due to them encroaching on
housing, e-flight is now the new normal. E-flight is no longer just indoor and park flyers &ndash; people are flying 20
pounds scale airplanes with the same authority as glow, and many of the Pattern and 3D Champions are flying
routines with electric aircraft!<br>
<br>
Since there are quite a few PDQ members interested in this sector of our hobby, I felt it might be worthwhile to
put down my e-flight learning into an introductory document &ndash; hopefully it will serve to demystify electric r/c
flight. There is a lot to learn in e-flight and I hope it won&rsquo;t scare off anyone, but at least some will be
fascinated and "charged up" by the renewed opportunity to learn new things.<br>
<br>
I am no expert on electric flight, but I have carefully (and seriously) researched this area in the past year and a
half. Everything I present in this article is what I have learnt and used, but obviously there is a whole lot more
to learn. The more I delve into it, the more I am fascinated by e-flight.<br>
<br>
SOME STARTING THOUGHTS<br>
<br>
Don&rsquo;t bother to compare electric motors and glow engines &ndash; you will tear out your (remaining) hair and get nowhere.
An apples-to-apples comparison is impossible! The same electric motor can be set up to work like a 15-size glow
engine, and also be set up to work like a 40! As of this moment, you must remember never to ask the question "so
what is an equivalent electric motor to a 40 size glow engine"!<br>
<br>
Do not be concerned about rpm. This is because the same electric motor can be set up with a propeller to scream at
30,000 rpm, and then be set up to run at a very sedate 7,000 rpm! This means the same electric motor can power a
2lb ducted-fan jet at high speed, yet fly a sedate Piper Cub at about 6lbs! Or anything in between! Just accept
that electric motors are a new type of engine, and don&rsquo;t try to understand them through comparison with what you
already know about glow engines.<br>
<br>
EQUIPMENT<br>
<br>
Besides the familiar receiver and servos, e-flight equipment consists of an electric motor, an electronic speed
controller (ESC), and a flight battery pack. For applications between 6 and 12 cell battery packs, the ESC has a
"battery eliminator circuit" (BEC) that powers the receiver and servos, without the need for an onboard receiver
battery pack. If you are using more than 12 cells in your flight pack, then you will need an onboard receiver
battery pack (just like glow), or use something called an "ultimate battery eliminator circuit" (UBEC). The UBEC is
only a few grams in weight, and connects to the flight battery pack to power the receiver and servos.<br>
<br>
Some ESCs have a receiver/motor switch, but you can run without one also. In e-flight you must always remember to
turn on your transmitter first before connecting the flight battery pack &ndash; this is to ensure that a nearby radio
doesn&rsquo;t swamp your receiver and turn the motor on.<br>
<br>
[SAFETY TIP: Never get close to the propeller when the flight pack is connected to the ESC &ndash; a radio problem can
start the motor at anytime, with disastrous consequences to you.]<br>
<br>
Lets look more closely at the flight equipment.<br>
<br>
ELECTRIC MOTORS<br>
<br>
Most people know how an electric motor works, so I won&rsquo;t waste time explaining it. There are two types of electric
motors &ndash; brushed, or brush-less. In a brushed motor, the coils are on the armature that rotates, and the magnets
are stationary. The power to the coils is provided through a set of brushes that contact the armature. This is its
weak link &ndash; the brushes wear out, cause sparking and radio interference, and generate a lot of heat. In a
brush-less motor, the magnets are on the armature that rotates, and the coils are stationary. There is no need for
brushes. Brush-less motors need little or no maintenance, and are much more efficient, so they produce much more
torque. Other than for indoor and park flyers, brush-less motors are the way to go (this is my own opinion of
course).<br>
<br>
The choice of a motor depends upon the Watts of power that you will need to fly your airplane (see later for a rule
of thumb). Respectable brushed motor manufacturers for e-flight are Plettenberg, Kyosho, Kontronik and Graupner &ndash; a
lot of these motors are badge-engineered by Mabuchi or Sagami. Respectable brush-less motor manufacturers are
Plettenberg, Kontronic, Mega, Axi, Astroflight, Aveox, Phasor, Hacker etc.<br>
<br>
ELECTRONIC SPEED CONTROLLERS<br>
<br>
The ESC provides throttle control by managing the current going to the motor, and can also provide power to your
receiver and servos (if you are using between 6 and 12 cells in your flight pack). The ESC connects to your
receiver throttle channel (and also provides power to the receiver through the same connection). One end of the ESC
connects to the flight battery pack, the other to the motor. ESCs have a voltage cutoff to the motor, to ensure
that you still have enough power to operate the receiver and servos once the battery power has been depleted in
flight. The voltage cutoff is usually 5V or thereabouts, which means there is plenty of voltage left to allow the
receiver to operate while you land dead stick. Like motors there are two types of ESCs &ndash; brushed, or brush-less.
They cannot be interchanged, i.e., a brushed ESC cannot power a brush-less motor, and vice versa. ESCs are rated
for the amperage of current they can handle &ndash; e.g., a 40amp ESC can deliver a constant 40 amps of current without
overheating. Although ESCs can deliver higher current than their rating for very short periods of time, you do not
want to exceed their rating. The choice of ESC depends upon the maximum current you will be delivering to your
motor (see later for calculations). Respectable brushed ESCs are Great Planes Electrifly, Castle Creations, Jeti,
Kontronic, GWS etc. Respectable brush-less ESCs are Jeti, Kontronic, Hacker, Schulze etc.<br>
<br>
FLIGHT BATTERY PACKS<br>
<br>
This is the source of power for your motor, receiver and servos. The number of cells determines the maximum voltage
available to the motor, and the combination of current and capacity of the cells determines the duration of the
flight. There are four major types of cells available right now &ndash; Nickel Cadmium (NiCd), Nickel Metal Hydride
(NiMh), Lithium Ion (LiIon) and Lithium Polymer (LiPoly). I know very little about the Lithium batteries, so I will
concentrate on the NiCd and NiMh batteries.<br>
<br>
NiCd and NiMh cells have been the mainstay of electric flight, and are still the easiest, safest and most reliable
cells available. LiPoly cells are definitely making significant inroads into electric flight and will one day
become the mainstay, but the NiCd and NiMh cells have not come to the end of their life. For a high current
application, you still cannot beat NiCd cells.<br>
<br>
NiCd and NiMh both have a nominal voltage of 1.2 volts per cell. The cells are connected in series to generate the
required voltage. For example, 6 cells will provide 7.2 volts (6 x 1.2v), 8 cells will provide 9.6 volts (8 x 1.2v)
and so on. When connected to a "load", the cells deliver less than 1.2v, but for our purposes it&rsquo;s okay to ignore
that for the moment. Battery packs are available in many different sizes and capacities. The cell capacity rating
is usually a number like 2400, or 1700 or 600 (many other numbers are available), and it represents the current
that the cell can deliver in one hour. For example a 2400 milliamp hour cell should be able to deliver approx. 2400
milliamps (or 2.4 amps) of current for 1 hour before it is discharged completely. In most cases, higher capacity
cells mean heavier cells.<br>
<br>
NiCd cells can deliver much higher amperage than NiMh cells, although NiMh cells are lighter, so there is a
trade-off. High discharge NiMh cells are also available, but they still don&rsquo;t seem to put out as strongly as NiCd
cells! Generally, for high current delivery (over 35 amps), NiCd cells work better. But you can significantly
increase flight times using NiMh because for the same weight, those cells have a lot more capacity. For example, a
7-cell 600mah NiCd pack weighs about the same as a 7-cell 1100mah NiMh pack.<br>
<br>
You can fast charge NiCd&rsquo;s at much higher currents without damaging them. A safe rule of thumb is that you can
charge a NiCd pack with an amperage that is equal to 3 times it&rsquo;s cell capacity rating &ndash; e.g., a 2400 milliamp hour
(2.4 amp hour) rated NiCd can be charged at 2.4 x 3 = 7.2 amps. For another example, a 600 milliamp hour (0.6 amp
hour) rated NiCd pack can be charged safely at 0.6 x 3 = 1.8 amps. A safe rule of thumb for NiMh is that you can
charge them at 2 times the cell capacity. [Most people charge NiMh at 1 or 1 ½ times the cell capacity, but I have
often read in European magazines that people have been safely charging at twice the cell capacity without any
long-term effect. I do this myself.]<br>
<br>
With repeated fast charging and discharging of a flight pack, NiCd cells tend to become unmatched, i.e., each cell
ends up at a different peak or discharged voltage. Therefore, from time to time they need to be discharged down to
about 0.8 volts per cell and then charged back to full capacity to ensure that each cell is brought back upto the
same voltage levels. This is called "cycling". IMPORTANT: But NiMh cells should not be discharged fully or they
will be damaged, and therefore should not be cycled.<br>
<br>
Both NiCd and NiMh cells should be stored fully charged when not in use &ndash; that&rsquo;s because they discharge (leak) over
time, and you don&rsquo;t want them to go below 0.8 volts per cell and be damaged permanently.<br>
<br>
There is a common perception that NiCd cells develop something called "memory", that is, they do not provide their
full capacity after repeated recharging without a full discharge. In actual fact, this phenomenon is caused more by
repeated over charging, which makes the cell peak voltages mismatched, and the cells weaker. We tend to use too
high a trickle charge current and often grossly overcharge battery packs. The whole pack then tends to behave the
same as it&rsquo;s weakest cell, and we see a drop in capacity. This is why it is important to use a "peak detect"
charger, which ceases charging as soon as the pack is fully charged. If you feel you MUST trickle charge your
batteries after they are full, please remember that the proper rate is 1/100th its capacity &ndash; for example, 2400mah
packs need to be trickled at 24ma or less, and 600mah packs need to be trickled at 6ma or less!<br>
<br>
Often, overcharged and unmatched NiCd cells will return to their original capacity if they are cycled a few times,
but if the overcharging continues regularly, ultimately they will not return to their capacity and will have to be
discarded. If you use a peak detect charger, you will be amazed at how long batteries last &ndash; I still have 6-cell
packs from my R/C car days (over 10 years ago!) that are in tip top shape, and I&rsquo;m using them in airplanes! They
have never developed "memory", and I regularly recharge them before they have been fully discharged.<br>
<br>
I have used NiCd cells that have been "zapped"! This is a process whereby each cell is connected to a very high
voltage for a very short period of time. It changes the internal chemistry of the NiCd and lowers its internal
resistance. This means that the cells can deliver higher currents. My research indicates there are no long-term
effects of zapping &ndash; time will tell I guess! Believe me when I say it, zapped NiCd cells dish out huge currents,
and the performance boost can actually be perceived in flight.<br>
<br>
I could go on and on about the merits and demerits of NiCd and NiMh. The best rule of thumb is this: if you are
wanting a high current application (i.e., over 35 amps) then go with NiCd, otherwise go with NiMh. If airframe
weight becomes an issue, then opt for NiMh cells of the same capacity, as they will be much lighter.<br>
<br>
One other thing to consider is this: if you are a meticulous person and care for your equipment, then NiMh&rsquo;s will
work for you; if not, then NiCd&rsquo;s are better for you because they take much more abuse and punishment, with respect
to charging and storage.<br>
<br>
My research indicates that Sanyo makes the best NiCd and NiMh cells, and Panasonic are pretty close behind in the
NiMh department. I have heard some really good things about KAN, but have not yet tried them.<br>
<br>
RECEIVERS<br>
<br>
Mini and Micro receivers work best for any electric application &ndash; there are many 4 to 6 channel, dual conversion,
micro receivers available. I use either the Hitec Micro 555 5-channel or the Hitec Electron 6-channel, and highly
recommend them. GWS also make micro receivers for small applications, though I don&rsquo;t have first hand experience
with them. I know others have used them with great success. There are quite a few single conversion micro receivers
available too, but I haven&rsquo;t used them. I have recently read that an electronics firm has come up with something
called Digital Signal Processing (DSP), and claims to provide glitch free, single conversion performance where all
other single conversion receivers have given up the ghost! But who knows!<br>
<br>
The only rule of thumb with respect to receivers in electric airplanes is that you should mount the receiver as far
away as you can from the flight battery pack and the ESC &ndash; both generate radio noise. Do NOT mount your receiver on
top of (or right next to) the battery pack or the ESC as you are asking for trouble! A lot of European flyers wrap
their receivers in aluminum foil (shiny side outward), and claim they have no electrical interference. I have tried
it, but cannot say for certain whether or not it works.<br>
<br>
For electric flight, as long as you have balanced your propeller, there is absolutely no need to wrap your receiver
in foam; electric motors do not create any vibration as long as the prop is balanced. I always use velcro to secure
my receiver in place &ndash; it will not come loose in flight unless it&rsquo;s a big 3D airplane, in which case it&rsquo;s better to
secure it with rubber bands as well.<br>
<br>
SERVOS<br>
<br>
The only servos I have used for e-flight are the Hitec HS-55, the Hitec HS-81 and the Dymond Mini. I highly
recommend them all, although it is now difficult to find the Dymond servos. GWS make Mini, Pico and Naro servos,
and I know that many e-flyers use them, though I cannot speak for them from personal experience. For park flyers
and airplanes less than 24 oz, the Hitec HS-55 servo is unbeatable. For larger applications, HS-55s can be used for
ailerons (one for each aileron). The HS-81 is very light yet has 36 oz in of torque, and can be safely used for
elevators and rudders in larger airplane. Larger electrics (5lb&rsquo;s or higher) should use standard size servos.<br>
<br>
COMMONLY USED MATHEMATICAL FORMULAE<br>
<br>
The most common mathematical formula you will use is P = I x V, where P is the power in Watts, I is the current in
Amps, and V is the voltage in Volts. For example, if an electric motor is drawing 40 amps of current while
connected to a 10 volt battery source, then it is developing 40 x 10 = 400 watts.<br>
<br>
Another common formula for calculating flight time is T = (60 x C)/I, where T is flight time in Minutes, C is the
cell capacity in AmpHour, and I is the current in Amps. For example, if you have a 2000 mah (i.e., 2 AmpHour)
battery pack, and you are drawing 40 Amps from it at full throttle, your flight time (at full throttle) will be (60
x 2)/40 = 3 minutes. As another example, if you have a 600mah (i.e., 0.6 AmpHour) battery pack, and you are drawing
8 Amps from it, your full throttle flight time will be (60 x 0.6)/8 = 4.5 minutes. [NOTE: actual flight times will
be greater than this, because you will not need to fly at full throttle for the whole time]<br>
<br>
Another common formula is L = (144 x W)/A, where L is the wing loading in oz/sq.ft, W is the flying weight of the
aircraft in Ounces, and A is the wing area in Square Inches. For example, an airplane with 500 sq.in of wing area
with a flying weight of 5 lbs (80 ounces) will have a wing loading of (144 x 80)/500 = 23.04 oz/sq.ft. As another
example, an airplane with 200 sq.in of wing area with a flying weight of 1 lb (16 ounces) will have a wing loading
of (144 x 16)/200 = 11.52 oz/sq.ft.<br>
<br>
Now that we are familiar with 3 important formulae used in electric flight, lets look at e-flight design.<br>
<br>
ELECTRIC AIRCRAFT DESIGN PARAMETERS<br>
<br>
The principles of aircraft design, whether glow or electric, are the same. What is more complicated in electric
flight, is choosing the right motor for the design. Hopefully, the following information will demystify some of
it.<br>
<br>
Design of an electric aircraft starts with the choice of a target wing loading. The following rule of thumb makes
the choice easier:<br>
<br>
5 &ndash; 10 oz/sq.ft &ndash; very light, slow flying, floater, no wind penetration [good for indoor]<br>
<br>
11 &ndash; 15 oz/sq.ft &ndash; light, medium speed, some wind penetration<br>
<br>
16 &ndash; 25 oz/sq.ft &ndash; fast flying, decent wind penetration<br>
<br>
26 &ndash; 35 oz/sq.ft &ndash; heavy, fast, excellent wind penetration<br>
<br>
[NOTE: the upper limit for comfortable hand launching is 20 oz/sq.ft]<br>
<br>
1. ASSUMPTION: For our purposes, lets decide on a 20 oz/sq.ft wing loading (because we still want to be able to
comfortably hand launch the aircraft)<br>
<br>
The next step in the design is to pick a wingspan, and calculate out the wing area. This depends, of course, on the
size of aircraft you want to build. For a scale airplane this would involve drawing out the plans for the target
wingspan and then calculating the wing area.<br>
<br>
2. ASSUMPTION: For our purposes, lets decide on a 50 inch wingspan, with a wing area of 500 sq inches<br>
<br>
If you look back at our commonly used formulae section, you will find one that says L = (144 x W)/A. We need to
adjust this so that we can find the target flight weight of our aircraft, since we have already picked our wing
loading and wing area. Our High School Mathematics tells us that the formula above can be re-written as W = (A x
L)/144, where W is the weight in ounces, A is the wing area in sq. inches, and L is the wing loading in oz/sq.ft.
Using our 500 sq.in area and 20 oz/sq.ft wing loading, our target weight will be (500 x 20)/144 = 69.4 ounces, or
approx. 4.3 lbs.<br>
<br>
It is now time for another handy rule of thumb with respect to the weight of an aircraft and the Watts needed to
fly it:<br>
<br>
50 watts per pound - some vertical performance (park flyers, trainers, non-aerobatic scale aircraft)<br>
<br>
75 watts per pound - good vertical performance (sport flyers, scale aircraft with some aerobatics)<br>
<br>
100 watts per pound - excellent aerobatic performance (precision aerobatics aircraft, scale aerobatic aircraft)<br>
<br>
125 watts per pound - unlimited vertical performance (competition precision aerobatics aircraft and 3D
aircraft)<br>
<br>
150 watts per pound - unlimited 3D performance<br>
<br>
[Note: the weight of the airplane is the all up weight, i.e., including motor, batteries and electronics]<br>
<br>
3. ASSUMPTION: We would like our aircraft to have very good vertical performance, so we want to achieve, say, 90
watts per pound.<br>
<br>
So, for our target weight of 4.3 lbs., it looks like we will need 4.3 x 90 watts = 387 watts.<br>
<br>
Going back to the "commonly used formulae" section, we now need to look at the one that relates power to current
and voltage. P = I x V, that is Power in watts = current in amps x voltage in volts. Using this formula, and our
target of 387 watts power, we can make this with many different combinations of current and voltage, as
follows:<br>
<br>
54 amps x 7.2 volts (6 cell pack)<br>
<br>
46 amps x 8.4 volts (7 cell pack)<br>
<br>
40 amps x 9.6 volts (8 cell pack)<br>
<br>
32 amps x 12 volts (10 cell pack)<br>
<br>
27 amps x 14.4 volts (12 cell pack)<br>
<br>
4. ASSUMPTION: We would like our aircraft to provide full throttle flight for 4.5 to 5 minutes (note that this
means as much as 8 minute flights, as full throttle will only be used on vertical aerobatics).<br>
<br>
Going back to our commonly used formulae section, the flight time formula is T = (60 x C)/I, where T is time in
minutes, C is cell capacity in amphours, and I is current in amps. Using this formula and a 1700mah cell capacity
(1.7amp hours), our 6 to 12 cell options shown above provide the following full throttle flight times:<br>
<br>
54 amps give us (60 x 1.7)/54 = 1.9 minutes<br>
<br>
46 amps give us (60 x 1.7)/46 = 2.2 minutes<br>
<br>
40 amps give us (60 x 1.7)/40 = 2.6 minutes<br>
<br>
32 amps give us (60 x 1.7)/32 = 3.2 minutes<br>
<br>
27 amps give us (60 x 1.7)/27 = 3.8 minutes<br>
<br>
Looks like 1700 mah cells are not going to do it for us, so we will have to try 2000 mah cells. As it turns out,
that will not work either, so we&rsquo;ll have to go to 2400 cells. Reworking the above equation for 2400 mah cells (2.4
amp hour) gives us:<br>
<br>
54 amps give us (60 x 2.4)/54 = 2.7 minutes<br>
<br>
46 amps give us (60 x 2.4)/46 = 3.1 minutes<br>
<br>
40 amps give us (60 x 2.4)/40 = 3.6 minutes<br>
<br>
32 amps give us (60 x 2.4)/32 = 4.5 minutes<br>
<br>
27 amps give us (60 x 2.4)/27 = 5.3 minutes<br>
<br>
Looks like we have two possible combinations that will work for us &ndash; a 10 cell 2400 pack, or a 12 cell 2400 pack.
The final choice will depend upon how good we are doing in the airframe weight, so we&rsquo;ll leave the choice till much
later. Since our two combinations are less than 35amps current, we can also use NiMh cells if we want. That comes
later!<br>
<br>
We have now reached our toughest decision, and that is, what motor to use. In my opinion, brush-less is the only
way to go if you are to have the same performance characteristics as glow engines. Even so, there are a huge number
of brush-less choices, and this is where it becomes difficult. To complicate matters, you can use gear drives to
drive a larger diameter and pitch propeller, trading rpm for torque. At this point, I will keep things simple and
choose a direct drive scenario (i.e., no gearbox).<br>
<br>
Time for a bit of theory! Electric motors can handle a wide range of Volts and Amps. However, the manufacturer
states the current range within which a motor is the most efficient (which means that it produces the best torque
and power for the current being sent through it&rsquo;s coils). For our purposes (27 amps and 32 amps), we are looking
for a brush-less motor that is the most efficient at a current range of 30 to 35 amps, and can take 10, 12 or more
cells. The best way to get this info is to get to the manufacturers (or retailers) web site and read their specs &ndash;
look for Plettenberg, Hacker, Mega, Axi, Phasor, Astroflight, Aveox etc. Axi makes an "outrunner" brush-less motor
in which the outer can rotates with the magnets attached to the inside. The can acts like a flywheel and generates
very high torque. In my opinion, Mega also has a fine line of brush-less motors that will fit the bill, and their
website provides all the data needed. They are easily the best value for money &ndash; they are a low buck brush-less
motor being developed in the Czech Republic, and are of excellent quality. If you want to go Cadillac, then
Plettenberg or Hacker would be your choices. Astroflight and Aveox also make good brush-less motors. You can also
find the website of Icare (an electric flight retailer based in Montreal) and their website provides data for
Plettenberg, Mega, Axi etc. The basic information you are looking for are: how many cells can the motor handle,
what is its maximum current rating, what is its most efficient current value or range, what sort of propeller sizes
are recommended, and what is its weight.<br>
<br>
Not to complicate matters, there is one other bit of information that is important for brush-less motors &ndash; the
number of winds in the coil. They range from 1 wind per coil to 5 or more winds. The rule of thumb is that for
high-speed applications (e.g., ducted fans, or racing applications) you need a smaller number of winds, and for
higher torque applications (e.g gearing and large airplanes) you need higher winds. A good compromise is to buy a 2
or 3 wind motor, so we can use it for both applications and be more flexible.<br>
<br>
So lets say we have chosen the motor based on the max. efficiency current of 30 to 35 amps; the important task now
is to record it&rsquo;s weight (again, the website provides this information). Now we need to choose an ESC. This is much
easier! We need to find an ESC that will handle 10 or 12 cells (those were our two possibilities in the power
calculations), provide a BEC, and be rated at a bit higher than the 35 amps we will put through it. In fact, 40
amps is a standard size, so that&rsquo;s what we should choose. It must be a brush-less ESC for our brush-less motor &ndash;
Jeti, Hacker, Castle Creations all manufacture good brush-less ESCs. I think Jeti is a good value for money. Again,
the websites will give us the weight of the ESC we have chosen, which we need to record.<br>
<br>
This is where you get out your gram scale (AN ESSENTIAL TOOL IF YOU WANT TO DO ANY SERIOUS ELECTRIC FLIGHT) and
weigh all the components &ndash; battery cells or whole pack, receiver, servos, motor, ESC etc. By the way, a digital
gram/ounce scale is the best of course. According to my scale, our airborne equipment will be as follows:<br>
<br>
2 HS-81 servos (elevator and rudder) at 0.6 oz each = 1.2 oz<br>
<br>
2 HS-55 servos (ailerons) at 0.2 oz each = 0.4 oz<br>
<br>
Hitec Electron 6-channel receiver = 0.8 oz<br>
<br>
Jeti 40 amp controller = 1.5 oz<br>
<br>
Motor = 7.6 oz (Mega brush-less)<br>
<br>
Prop/spinner = 2 oz<br>
<br>
This makes a total of 13.5 oz.<br>
<br>
Now lets get to the battery. A 10-cell pack of Sanyo CP2400SCR NiCd cells weigh 21 oz. A 10-cell pack of Sanyo
GP3300 NiMh cells weighs 22.8 oz. Obviously, it&rsquo;s worth going for the NiMh cells for the additional flight time
they will provide, as there is only a 1.8oz weight penalty. Just to verify, using our flight time formulae, the
2400 NiCd will provide (60 x 2.4)/32 = 4.5 minutes, whereas the 3300 NiMh will provide (60 x 3.3)/32 = 6.2
minutes!<br>
<br>
So now our airborne equipment weight has reached 13.5 + 22.8 = 36.3 oz. Since our target weight was 69.4 oz, this
leaves us 33.1 oz for the airframe and finishing. That&rsquo;s a bit over 2 lbs for a target airframe weight.<br>
<br>
So our challenge is to build a 50 inch wing span, 500 square inch wing area airplane which will have an empty
weight (including finishing) of 33.1oz. Perfect time to discuss building techniques for electric flight!<br>
<br>
BUILDING TECHNIQUES FOR ELECTRIC AIRPLANES<br>
<br>
In building airframes for glow engines, we have always been cognizant of engine vibrations. A lot of plywood is
used to ensure the model does not deteriorate from the effects of vibration. People tend to correlate strength with
weight, so their quest to build a strong airframe ends up in grossly overweight airframes. Building for vibration
is completely unnecessary in electric flight, because there is no vibration (as long as you balance your prop of
course). Because of this lack of vibration it is also possible to make use of foam parts that are amazingly strong
yet incredibly light. You don&rsquo;t need to have fiberglass cowls, since ABS cowls will easily withstand the test of
time without any vibration, and think of the weight that it can save!<br>
<br>
The keyword in electric building is lightness and engineered strength &ndash; remember light doesn&rsquo;t have to mean flimsy!
Lets be clear that strength does not necessarily mean weight! For example, two balsa sheets laminated with their
grain perpendicular to each other is stronger than an equivalent size and thickness of light ply, yet half the
weight! Ribs can be the same strength yet half the weight, if there are circular lightening holes cut out from
them.<br>
<br>
Firewalls for electric motors need only be made from 1/8 inch. plywood (and at most ¼ in plywood for large
airplanes) and you don&rsquo;t need heavy motor mounts or bearers to mount them.<br>
<br>
Finishing foam or sheeted parts with ¾ oz glass cloth and thinned down epoxy with a spray coat of acrylic paint is
pretty light (remember, heavy epoxy paints are not needed in electric flight, because there is no glow fuel to
attack it!). And of course the old faithful Monokote always works! The makers of Solarfilm are now making an
iron-on covering that is ½ the weight of Solarfilm or Monokote!<br>
<br>
For electric airplanes, thin and medium cyanoacrylate glue are the only adhesives required &ndash; due to the lack of
vibration, medium zap is more than adequate for firewalls. Foam parts are best adhered with Weldbond polyurethane
glue which is incredibly strong and incredibly light &ndash; this works well for foam-to-foam as well as wood-to-foam
joints. A lot of ARF electric kits use fiberglass fuselages &ndash; they are strong and incredibly light.<br>
<br>
Enough said on building airframes; a 2 lb airframe for a 50in. span, 500 sq in wing area airplane should be easily
achievable, without sacrificing any strength at all.<br>
<br>
Back to our aircraft design….<br>
<br>
To complete our discussion on design parameters, if it were impossible to meet the airframe weight goal of 2 lbs
for a 50in span 500sq in wing area airplane, then we would have to go back to our original assumption #2 and repeat
our calculations for a larger wing area. An increase in wing area adds very little airframe weight, but allows a
higher target weight with the same wing loading as before. Since our airborne components are still the same weight,
we end up with a higher airframe target weight, which is easier to achieve.<br>
<br>
PROPELLERS<br>
<br>
We finally come to the last part in the process… propellers. Electric propellers are very different from glow
propellers. Since electric motors don&rsquo;t have comparable torque to glow engines, the propellers have to be designed
for much higher efficiency &ndash; as a result they are much lighter, have more accurate airfoil cross sections, and
often have tips designed to reduce drag. Most electric propellers are not the same pitch throughout &ndash; i.e., they
change pitch the further out you go towards the tip. Electric and glow propellers are not interchangeable &ndash; a glow
propeller mounted on an electric motor will be substantially underpowered, as they are much heavier, and the
electric motor draws a lot more current to turn such a heavy load. An electric propeller mounted on a glow motor
could be dangerous, as the lighter prop might flex too much and disintegrate!<br>
<br>
Electric propellers come in two varieties &ndash; fixed or folding. Folding propellers were designed because a lot of
electric airplanes do not have landing gear, and land on their belly. This can sometimes break the propeller, or
much worse it can bend the shaft on the smaller motors. Graupner folding propellers are universally considered the
best. Graupner also make a full line of fixed propellers and precision spinners. APC and Master Airscrew also make
electric props.<br>
<br>
So, how does one decide what propeller to use? The starting point should be what the manufacturer recommends for
the motor &ndash; this data is usually available in the instruction sheets that come with the motor, or are available on
the manufacturer web sites. Since the manufacturer does not know what application you are using the motor in, it is
only a starting guideline. To get it right, you MUST have a device called an Astro Whatt Meter (yes, it&rsquo;s called
Whatt meter, not Watt meter!). THIS IS AN ESSENTIAL PIECE OF EQUIPMENT IF YOU ARE GOING TO DO ANY SERIOUS ELECTRIC
FLIGHT. The Whatt meter installs between your flight battery pack and your ESC, and displays the following
information in real time &ndash; the actual voltage that is going to the motor, the current that is being drawn by the
motor, the input power (in watts) that is being generated, and the amount of milliamp hours that has been expended
from the battery.<br>
<br>
The only way to find the right propeller for your setup is to fully charge your flight pack, hook up the Whatt
meter, run the motor at full throttle, and check the current that is being drawn. Remember back in the motor
selection section we talked about the maximum efficiency current of the motor? Well, the bottom line is that you
have to find the right diameter and pitch propeller that will draw the maximum efficiency current for the motor. In
our example above, we had chosen a motor whose maximum efficiency current was between 30 and 35 amps. So, we need
to find a propeller that will draw approximately 32 amps at full throttle (split the difference between 30 and 35
amps), at the start of the fully charged flight pack.<br>
<br>
<br>
The process to follow is to start with the manufacturers recommended prop, determine the current that is flowing at
full throttle using the Whatt meter, with a fully charged flight battery. In our example, if it&rsquo;s less than 32
amps, then you should either increase the diameter or the pitch so that the motor is forced to draw more current to
drive the heavier loading. In our example, if the motor is drawing more than 32 amps, then either the diameter or
the pitch must be reduced to reduce the current draw. Of course, the same rules as glow props apply, that is,
bigger diameter gives you more power and vertical performance, and a higher pitch gives you more speed.<br>
<br>
GEARING<br>
<br>
Gearing allows a motor to draw the same current, yet turn a much larger diameter and larger pitch propeller. Put
another way, gearing allows the motor to trade rpm for torque. As a result, the motor can still be running at it&rsquo;s
maximum efficiency rpm (thus producing peak power) yet can turn a much larger propeller. This lets us use a small
motor designed to operate efficiently at a high rpm, to power a large aircraft.<br>
<br>
As an example, a motor might be turning a 7 x 5 propeller in direct drive mode, and drawing 30 amps to do it. If it
were geared, it would be able to turn a 10 x 8 propeller and still be drawing 30 amps. A larger diameter propeller
means more thrust, which means better performance for the same aircraft, OR being able to fly a heavier
airplane!<br>
<br>
Alternatively, with suitable gearing we can equip a small airplane with a very large propeller that produces high
thrust at low speeds, which is ideal for 3D aerobatics, or indoor flight.<br>
<br>
Gearing adds very little weight to the system, and increases thrust dramatically. Not sure why gearing didn&rsquo;t catch
on in glow engines, but it is pretty much the norm for larger electric airplanes.<br>
<br>
There are many different gearing ratio&rsquo;s &ndash; e.g., 2 to 1, 3 to 1, 5 to 1 and so on. So, for a 2 to 1 ratio it means
that for every 2 revolutions of the motor, the propeller turns one revolution. I haven&rsquo;t yet figured out if this
means that the torque is doubled, but I guess I have some more learning to do! I know that if I am to progress into
larger, heavier airplanes (that is, greater than 4 or 5 pounds) then I will have to get very friendly with someone
who knows about gearing! I also know that geared electric motors are now powering airplanes over 20 pounds, so the
sky is the limit.<br>
<br>
I hope this article has given you some insight into one more facet of our wonderful hobby.</p>
