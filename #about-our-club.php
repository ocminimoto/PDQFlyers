<h4>History</h4>

<p>The Club was formed in 1982 by Bill Martin and Marty Nash. Bill Martin, Brian Price, and Bill Rollins are now
our only original members.</p>

<p><img title="BIll Martin"
     style=
     "BORDER-TOP: #ffffff 8px solid; HEIGHT: 195px; BORDER-RIGHT: #ffffff 8px solid; WIDTH: 260px; BORDER-BOTTOM: #ffffff 8px solid; BORDER-LEFT: #ffffff 8px solid"
     border="0"
     alt="BIll Martin"
     src="images/Bill%20Martin-800.jpg"
     align="left">Bill Martin provided us with a description of the club's origin:</p>

<p>"In 1980, I put a large model air show on at the then deactivated Boundary bay Airport in Delta. The Lions Club
provided the canteen and contributed to my charity, the Heart Foundation. Because of its success the Lions club in
Parksville recruited me to help them do one here in 1981. It was virtually rained out but some flights were made. I
was looking to retire here and Martin Nash lived here part of the time so we linked up to take names to form a club
at the 1982 Air Show held in a field across the highway, opposite the now named Wembly Mall. I think we signed up
about 8 or 9 members and the club grew from there. We held meetings at members houses, struggled to find flying
sites of which Bill Rollins was a key player. Marty and I made an agreement that because we were outsiders so to
speak (members of the RCFCBC on the mainland), we would never take office, leaving it for the locals to run in
their territory, That is true to this day".&nbsp;</p>

