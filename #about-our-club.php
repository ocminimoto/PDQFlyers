<h4 align="left">Meetings</h4>

<p>PDQ Flyers hold monthly meetings to discuss Club business and activities. There is usually time for a
presentation on some aspect of our hobby, which might include: model construction tips or demo, flying, radio
equipment or a persons personal experiences.<br>
<br>
It is an opportunity to meet other club members, introduce yourself as a potential new member, or bring up items of
interest or concern to the club.<br>
<br>
These meetings are held at 7:00 pm, on the second Wednesday of every month, at Parksville Community Centre, 132 E.
Jensen Ave., Parksville.&nbsp; The July and August meetings are held at our flying field.</p>

