<h2>RSS Access</h2>

<p>An RSS feed (Really Simple Syndication) is an easy to use way of showing new items on a website without
requiring that the reader go to that website to check for changes. Many different types of sites provide these
because the feeds are easy to pick up and you can aggregate the ones you want to see on a single page.</p>

<p>Newspapers are a great example. You can see headlines at a glance and click on them if you would like to see
more.&nbsp;You can&nbsp;use an aggregator&nbsp;like <a href="http://www.ighome.com/"
   target="_new">igHome</a> for all of&nbsp;your feeds and set it as the home page on&nbsp;your browser. You can
   select from thousands of feeds; various news outlets, comics, games, and other websites that&nbsp;you
   might&nbsp;like to&nbsp;keep in touch with&nbsp;as well.</p>

<p>We&nbsp;have set up a PDQ Flyers RSS Feed and update it regularly with things that are happening.&nbsp; If you
would like a message posted on it, send me an email and I can do that.</p>

<p>The PDQ Flyers feed can be added to your newreader by clicking on the link below. It will direct you to the
source of the PDQ Flyers RSS Feed (shown above). If you copy the URL for that page, you can paste it into
the&nbsp;aggregator that you have chosen to use. (If that is igHome, cause your cursor to hover over the sprocket
on the top right and select "Add RSS Feed". Paste the URL into the box provided and it will add the PDQ Flyers RSS
Feed to your window).</p>

