<td class="XSP_CENTER_PANEL" rowspan="2"><table class="XSP_CENTER_PANEL" cellpadding="0" cellspacing="0" border="0"><tr><td class="XSP_MAIN_PANEL"><p class="title"><font color="#0000ff"><strong><span style="FONT-SIZE: 20pt">Training
Program</span></strong></font></p>

<p><span style="FONT-SIZE: 12pt">PDQ Flyers&nbsp;recognize four levels of flying skill:</span></p>

<ul>
    <li><span style="FONT-SIZE: 12pt">Trainees, who&nbsp;are not allowed to fly solo at the Club field (with one
    exception described below);</span></li>

    <li><span style="FONT-SIZE: 12pt">Park Flyers, who are qualified to fly aircraft meeting the definition of Park
    Flyers below;</span></li>

    <li>Unlimited flyers, members who have been certified under our "PDQ Wings" program; and</li>

    <li>Instructors, members who are achieved a level of skill that qualifies them to teach others.</li>
</ul>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">Park Flyer models are&nbsp;aircraft that weigh under two
pounds, have less than 48 inch wingspan, fly relatively slowly, and are electric powered.</p>

<h3>Park Flyers</h3>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">Achieving the Park Flyer designation&nbsp;is done&nbsp;through
certification&nbsp;by&nbsp;a club Instructor.&nbsp; You must demonstrate that you can fly your model safely, take
off and land, keep out from the flight line, and in general have control of your model at all times.&nbsp; If you
believe that you have the flying skills to do these things, contact the <a href=
"mailto://NewMemberDirector@pdqflyers.com"
   target="_new">New Member Director</a> who will arrange for an instructor to conduct a test.</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">If you need assistance prior to such a test or you are unable
to demonstrate the requisite&nbsp;control during the examination, you will have to arrange time through the
   <a href="mailto:NewMemberDirector@pdqflyers.com"
   target="_new">New Member Director</a>&nbsp;for a club&nbsp;Instructor&nbsp;to provide&nbsp;buddy box
   training.</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">When you have exhibited the flying skills described above to
the Instructor's satisfaction you will receive the Park Flyer designation on your Membership Card indicating that
you are qualified to fly models that meet the park flyers description.&nbsp; These are the ONLY models that you may
fly without the assistance of an Instructor.</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">While the Club does not encourage it,&nbsp;if&nbsp;you are
a&nbsp;current PDQ&nbsp;member and there are no other flyers or spectators present,&nbsp;you&nbsp;may fly Park
Flyers at the club field&nbsp;prior to achieving&nbsp;your&nbsp;Park Flyer status.&nbsp; If you wish to fly when
others are present you must attain that status.&nbsp;</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">If, or when, you&nbsp;are proficient&nbsp;and wish to upgrade
to a larger electric, glow, or gas powered model, you must then pass the&nbsp;PDQ Flight Test to obtain your PDQ
Wings designation or&nbsp;request training that will allow you to do so.</p>

<h3>PDQ Wings</h3>

<p>To fly any aircraft larger than Park Flyers, you will need&nbsp;to&nbsp;have a membership card indicating that
you are certified with your&nbsp;PDQ Wings designation.&nbsp;</p>

<p>For new Members to PDQ Flyers who are coming to us from another Club and are already skilled flyers, a simple
check flight with one or our instructors is sufficient to allow certification.&nbsp; For new members who are also
new to flying, the balance of this section is applicable.</p>

<p>Certification can take place at any time that&nbsp;a Club&nbsp;Instructor agrees that you are ready, which may
or may not require&nbsp;preparatory training.&nbsp;&nbsp;Contact one of the Instructors to make that determination
and&nbsp;the Instructor will make arrangements for you&nbsp;to be tested if it is appropriate.&nbsp;</p>

<p>When the&nbsp;two testing Instructors are&nbsp;satisfied that your flying skills&nbsp;meet the required
criteria, you will receive a&nbsp;Membership Card indicating that you have attained your&nbsp;PDQ Wings
and&nbsp;are qualified to fly any aircraft type at our field.&nbsp;</p>

<p>The PDQ Wings certification is described on our <a id="D#PDF-Files/Wings Program.pdf"
   style="TEXT-DECORATION: underline; COLOR:"
   href="PDF-Files/Wings%20Program.pdf"
   target="_new">PDQ Wings program</a>.&nbsp; If you have been accepted into a PDQ Wings training program, you
   should print this document and&nbsp;have it with you at each training session.</p>

<h3>Training Considerations</h3>

<p>We&nbsp;sometimes have more training needs than trainer time available, particularly at the beginning of the
season.&nbsp; For this reason, it is important that Trainees prepare for their training sessions as much as they
can.&nbsp;&nbsp;Club Instructors&nbsp;expect at least the following.</p>

<h4><em>Computer Flight&nbsp;Simulators</em></h4>

<p>There are many simulators on the market that are reasonably priced and which are extremely helpful in getting
you familiar with transmitter actions and plane responses.&nbsp; Time spent practicing in this artificial
environment where no damage is ever done by crashing&nbsp;can reduce your at-the-field&nbsp;learning time
significantly and therefore the time needed&nbsp;from an Instructor.</p>

<h4><em>Equipment Preparation</em></h4>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">A&nbsp;student is expected to arrive at the field for a
training sesson&nbsp;on&nbsp;time and with his aircraft and field equipment, as close as possible, in ready to fly
condition.&nbsp; It is NOT&nbsp;ok to get your new&nbsp;ARF in a box on Thursday, throw in the servos and engine on
Friday, and expect to arrive at the field on Saturday ready for flight training.</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">It is clear that a new flyer can only go so far when it comes
to having his equipment totally prepared for flight. &nbsp;However, the instructions for assembly must be totally
understood and followed. &nbsp;The aircraft must be properly balanced.&nbsp; For glow engines, the instruction
manual must be read, reread, and all instructions considered.&nbsp; The engine must have at least two tanks of fuel
run through it and the student must understand a little bit about engine adjustments.&nbsp; (A perfect place to run
the engine and minimize noise discomfort to others is behind the container at the flying field).&nbsp;&nbsp;For
both glow and electric, all&nbsp;batteries must be fully charged and tested.</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">Naturally, with a new student, the instructor will check the
student's work and adjust or correct as necessary but the message here is that the&nbsp;trainer is a flight
instructor, not a teacher of the basic fundamentals of model aviation.</p>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">NOTE: Flight stabilization technology can help you learn to fly
and it is acceptable to use it during training sessions (with the agreement of the Instructor).&nbsp; However, it
must be turned OFF&nbsp;for any&nbsp;qualification tests.</p>

<h4 style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%"><em>Buddy Boxes</em></h4>

<p style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%">Prior to flight training the issue of the secondary training
transmitter, commonly called the buddy box must be addressed.&nbsp;&nbsp;Both the student's and Instructor's
transmitters must be compatable and as there are now many different technologies available, this can present an
issue.&nbsp; PDQ Flyers is equipped handle Spektrum and Futaba 2.4GHz equipment only, so if you have not yet
purchased your equipment, it would be wise to contact the <a href="mailto:NewMemberDirector@pdqflyers.com"
   target="_new">New Member Director</a> to discuss options.&nbsp; If your equipment is not compatable with the
   Club's, it will be necessary to work out alternate arrangements.</p>

<h4 style="MARGIN-BOTTOM: 0.35cm; LINE-HEIGHT: 115%"><em>MAAC Guidance</em></h4>

<p><span style="FONT-SIZE: 12pt">The MAAC website has training materials that might be consdered:</span>
<span style="FONT-SIZE: 12pt"><a style="TEXT-DECORATION: underline; COLOR:"
   href="http://www.maac.ca/en/learn_to_fly.php"
   target="_new">MAAC Learn to Fly</a></span>&nbsp;</p>

<h3>Instructors</h3>

<p>Our Club is blessed with a number of highly skilled pilots and some of them have agreed to donate some of their
time training others.&nbsp; There is no formal certification for Instructors, instead&nbsp;they are&nbsp;designated
by the existing Instructors through their personal interactions with&nbsp;our members.&nbsp;</p>

<p>You can make arrangement with one of the Instructors&nbsp;for certification or training, or if you are a new
member, you can talk to the <a href="mailto:NewMemberDirector@pdqflyers.com"
   target="_new">New Member Director</a> to do that.</p>
