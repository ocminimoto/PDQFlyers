<p>It was Bipes and Stunts day at the PDQ Field on Saturday, July 21, and despite the gusty winds there were no
accidents. Those who took part got a good taste of what it's like to take part in an aerobatics competition and a
chance to brush up on their piloting proficiency. One little wrinkle, brewed up by the contest directors, was
flying a pattern on the club simulator using the club trainer. And, it wasn't as easy as it seems.</p>

<p>Thanks to Craig MacQuarrie and Leo Katila for putting on the competition. For all those members who showed up,
it was a fun day and again thanks to Mike Hooper, our PDQ Grillmaster, for the excellent tube steaks on a bun.</p>

