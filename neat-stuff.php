<h4>World R/C Speed Record -468 mph - with no engine!</h4>

<p>Using a technique called dynamic soaring that was copied by watching the actions of Turkey Vultures, you can
obtain very high speeds. I found a couple of videos on the web, one of which shows the world record speed of 468
mph being set. However, it isn't a very good video in that you can't see the glider very well. This video is very
good and show how the technique works.</p>

<p>Take a look at this YouTube video <a title="Dynamic Soaring"
