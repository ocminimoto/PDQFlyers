<h4>Jack's Ride</h4>

<div style="FONT-FAMILY: TimesNewRomanPSMT">
    <p style="MARGIN: 0px 0px 1em; LINE-HEIGHT: 25px"><img title="Air Taxi"
         style=
         "HEIGHT: 262px; BORDER-TOP-COLOR: ; WIDTH: 350px; BORDER-LEFT-COLOR: ; BORDER-BOTTOM-COLOR: ; BORDER-RIGHT-COLOR:"
         border="0"
         hspace="5"
         alt="Air Taxi"
         src="images/airtaxi.jpeg"
         align="right">Range anxiety, the bugaboo of all-electric driving, is even more frightening for
         all-electric flying, where running out of power has worse consequences than having to pull over to the
         side of the road.</p>
</div>

<p style="MARGIN: 0px 0px 1em; LINE-HEIGHT: 25px">A solution now comes from <a style=
"TEXT-DECORATION: underline; COLOR:"
   href="http://workhorse.com/surefly"
   target="_new">Workhorse</a>, an Ohio-based firm. It has a passenger-carrying air taxi, called the SureFly, which
   combines the company&rsquo;s expertise in partially automated operation, from its drone business, and in
   hybrid-electric propulsion, from its truck business.</p>

<div style="FONT-FAMILY: TimesNewRomanPSMT">
    <p style="MARGIN: 0px 0px 1em; LINE-HEIGHT: 25px">The craft&rsquo;s eight counter-rotating motors each drive a
    carbon-fiber rotor, and the power comes from a generator cranked by an internal-combustion engine. You can fly
    110 kilometers (70 miles) on a tank, then refill in minutes. There&rsquo;s also a small lithium-ion battery, as a
    backup.</p>

    <div>
        Now that&rsquo;s my kind of taxi. When it becomes self-driving, I can take Fran along.
    </div>

